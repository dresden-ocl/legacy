package tudresden.ocl20.pivot.language.ocl.staticsemantics;

public class OclStaticSemanticsException extends Exception {

	private static final long serialVersionUID = -6108093169943998231L;

	public OclStaticSemanticsException() {
		super();
	}

	public OclStaticSemanticsException(String message) {
		super(message);
	}

	public OclStaticSemanticsException(Throwable cause) {
		super(cause);
	}

	public OclStaticSemanticsException(String message, Throwable cause) {
		super(message, cause);
	}

}
